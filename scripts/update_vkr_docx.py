def set_cell_text(cell, text):
    cell.text = text


def update_table(table_index, rows, doc):
    table = doc.tables[table_index]
    while len(table.rows) > 1:
        table._tbl.remove(table.rows[-1]._tr)
    for method, purpose in rows:
        row = table.add_row()
        set_cell_text(row.cells[0], method)
        set_cell_text(row.cells[1], purpose)


def update_test_table(table_index, rows, doc):
    table = doc.tables[table_index]
    while len(table.rows) > 1:
        table._tbl.remove(table.rows[-1]._tr)
    for scenario, expected, result in rows:
        row = table.add_row()
        set_cell_text(row.cells[0], scenario)
        set_cell_text(row.cells[1], expected)
        set_cell_text(row.cells[2], result)


